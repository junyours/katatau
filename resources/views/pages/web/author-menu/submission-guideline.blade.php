      <h1 class="font-semibold">
        Plagiarism Policy for Manuscript Submission and Publication
      </h1>
      <p>
        The purpose of this policy is to ensure academic integrity and
        maintain the quality of work published by DisKURSO International
        Multidisciplinary Research Journal. Plagiarism is strictly
        prohibited and will not be tolerated. This policy outlines the
        procedures for checking manuscripts for plagiarism and the
        consequences for any violations.
      </p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">Definition of Plagiarism</h1>
      <p>
        Plagiarism refers to the practice of using someone else's work,
        ideas, or intellectual property without proper acknowledgment or
        permission. This includes, but is not limited to, copying text,
        ideas, or data without citation, paraphrasing others’ work
        without appropriate attribution, and submitting someone else’s
        work as one’s own.
      </p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">
        Manuscript Submission and Checking Process
      </h1>
      <div class="space-y-1">
        <h1 class="font-semibold whitespace-nowrap">
          1. Initial Submission:
        </h1>
        <p>
          Authors submitting manuscripts must ensure that the content
          is original and properly cited. Any text, data, or ideas
          that are borrowed from other sources should be clearly
          referenced according to the publication's citation
          guidelines.
        </p>
      </div>
      <div class="space-y-1">
        <h1 class="font-semibold whitespace-nowrap">
          2. Plagiarism Screening:
        </h1>
        <div>
          <p>
            o Upon receipt of a manuscript, it will undergo a
            plagiarism check using a reputable plagiarism detection
            tool (e.g., Turnitin, iThenticate).
          </p>
          <p>
            o The manuscript will be assessed for similarity against
            published sources and other submitted papers within the
            database.
          </p>
        </div>
      </div>
      <div class="space-y-1">
        <h1 class="font-semibold whitespace-nowrap">
          3. Evaluation of Results:
        </h1>
        <div>
          <p>
            o If the plagiarism check reveals a similarity index
            above 20%, the manuscript will be reviewed further by
            the editorial team to determine the extent and context
            of the matches.
          </p>
          <p>
            o The editorial team will consider whether the
            similarities are properly cited, if they constitute an
            infringement on academic integrity, or if the work
            includes content that is excessively similar to other
            sources without sufficient originality or citation.
          </p>
        </div>
      </div>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">Consequences of Plagiarism</h1>
      <div class="space-y-1">
        <h1 class="font-semibold whitespace-nowrap">
          1. Minor Infractions:
        </h1>
        <div>
          <p>
            o If the plagiarism is found to be unintentional or
            minor (e.g., improper citation or a small degree of
            overlap), the authors will be notified and given an
            opportunity to correct the manuscript by providing
            proper citations or making revisions.
          </p>
          <p>
            o The manuscript may be sent back for revision and
            resubmission.
          </p>
        </div>
      </div>
      <div class="space-y-1">
        <h1 class="font-semibold whitespace-nowrap">
          2. Major Violations:
        </h1>
        <div>
          <p>
            o If the plagiarism is substantial or deliberate, the
            manuscript will be rejected, and the authors will be
            informed that their submission has been disqualified due
            to plagiarism.
          </p>
          <p>
            o Authors found to have committed serious plagiarism may
            be banned from submitting future manuscripts to DisKURSO
            IMRJ.
          </p>
        </div>
      </div>
      <div class="space-y-1">
        <h1 class="font-semibold whitespace-nowrap">
          3. Retraction of Published Articles:
        </h1>
        <div>
          <p>
            o If plagiarism is detected after the manuscript has
            been published, the article will be retracted, and a
            notice of retraction will be published in the journal.
          </p>
          <p>
            o The author(s) will be informed of the decision, and
            further action may be taken depending on the severity of
            the plagiarism.
          </p>
        </div>
      </div>
