  <div class="font-medium text-justify space-y-4">
    <h1 class="font-bold text-base border-b border-slate-300 pb-4">Research Ethics</h1>
    <p>The DisKURSO International Multidisciplinary Research Journal (DisKURSO IMRJ) adheres to the highest standards of
      research
      ethics to maintain the integrity and credibility of published works. Authors, reviewers, and editors are expected to
      abide by the following ethical guidelines:</p>
    <div class="space-y-2">
      <h1 class="font-semibold">1. AI in Research</h1>
      <p>The use of Artificial Intelligence (AI) tools in research must comply with ethical standards to ensure
        transparency and reliability. Authors must disclose any use of AI in their research processes, particularly in
        data collection, analysis, or writing. AI tools should complement human expertise, not replace critical thinking,
        and must not be used to fabricate or falsify results. All AI-generated content should be properly attributed to
        avoid issues related to authorship and plagiarism.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">2. Ethical Treatment of Animals in Research</h1>
      <p>Research involving animals must adhere to ethical guidelines that prioritize their welfare. Researchers must
        ensure compliance with national and international animal welfare standards and acquire necessary ethical approvals
        from relevant institutions. The use of animals in research should be justified by clear scientific objectives, and
        researchers must minimize harm and suffering, applying the principles of the 3Rs: Replace, Reduce, Refine. All
        animal research should be clearly stated, with methods and protocols made transparent in the manuscript.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">3. Authorship and Contributions</h1>
      <p>The authorship of a research paper should be based on substantial intellectual contributions to the research,
        including its conception, design, execution, analysis, and interpretation of results. All authors must be
        appropriately credited for their contributions, and no one should be included as an author who does not meet these
        criteria. Authors must ensure that there are no conflicts of interest and that all contributors have agreed to the
        final manuscript. Any disputes regarding authorship should be resolved before submission.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">4. Plagiarism and Originality</h1>
      <p>Authors must submit original research and ensure proper citation of prior work. Plagiarism, whether intentional
        or unintentional, is strictly prohibited. Manuscripts should not contain text, data, or ideas derived from other
        sources without appropriate attribution. All sources used in the research should be acknowledged, and any
        instances of potential conflicts of interest must be disclosed.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">5. Confidentiality</h1>
      <p>All data provided by participants in human or animal studies must be kept confidential and used only for the
        purposes of the research. Researchers should respect the privacy of participants and ensure informed consent is
        obtained before data collection begins. Confidential information should not be disclosed to unauthorized persons
        during or after the study.</p>
    </div>
    <p>By following these ethical guidelines, DisKURSO IMRJ aims to foster responsible, transparent, and impactful
      research that adheres to the highest academic standards.</p>
  </div>
@endsection