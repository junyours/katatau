      <h1 class="font-semibold">Publication Frequency</h1>
      <p>The publication frequency of this journal is quarterly, meaning that new issues are released four times a year.
        This schedule ensures regular dissemination of research findings, trends, and advancements in the field, while
        providing ample time for peer review, editorial processes, and content development. By adhering to a quarterly
        cycle, the journal maintains a balance between timely publication and high-quality, well-curated.</p>
      <p>All manuscripts need to be written in English. A4 manuscripts should not include more than 30 single-spaced A4
        pages (font size 12, 1" margins), and they should not have more than 10 tables, figures, or pictures. The
        references page would be included in the total number of pages. Generally, manuscripts are printed in black and
        white, however, authors can opt colored printed content subject to additional charges. If the manuscripts are
        accepted for publication, they must be made available in Microsoft Word format.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">1. Title</h1>
      <p>The title should be written by the author/s in a manner that provide/s enough information for the reader to
        determine whether the paper is likely to be of interest.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">2. Author(s)</h1>
      <p>Name(s) of authors, complete postal address(es) of affiliations and E-mail of the corresponding author.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">3. Abstract and Keywords</h1>
      <p>The abstract should satisfy the requirements of the research expert or researcher and serve as a credible
        literary supplement to the scientific report. Full-length papers should have no longer than 250 words, while notes
        and short pieces should have abstracts no more than 75 words. The abstract's final section should contain a list
        of indexing keywords.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">4. Introduction</h1>
      <p>Ideally, introductions should be brief. The following are essential components of a strong introduction:
      <p>(1) a concise description of the problem that justifies the work or the hypothesis that on which it is based.</p>
      <p>(2) the problem statement that will be explored and tested.</p>
      <p>(3) an explanation of the overall goals, objectives, and strategy of the study. Exciting and engaging the reader
        should be the goal of the introduction.</p>
      </p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">5. Methodology</h1>
      <p>This section provides details regarding the materials, methods, experimental design and other related
        information. It is important to include enough information so that the reader may replicate the tests. A
        chronological order, a list of approaches, or any other arrangement that will best help the reader can be used in
        this section.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">6. Results</h1>
      <p>To clearly convey the findings, use tables, graphs, diagrams, and/or pictures. Significant findings should be
        highlighted, but data from tables and visuals shouldn't be covered in detail in the text. Demonstrate how the
        goals were accomplished. The outcomes must be related to each other. Labels for graphs, diagrams, pictures, and
        table titles should be presented using APA format.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">7. Discussion</h1>
      <p>The author evaluates the results' significance in the discussion section. Describe how the outcomes address the
        issue that was mentioned in the introduction or as the goal. Link this study's findings to earlier research to
        demonstrate how and why they agree or differ. Indicate potential future developments and highlight the work's
        importance and ramifications. Avoid offering justifications for unforeseen outcomes or experimental failures.
        Discussions about contentious topics should be fair and transparent. Results that deviate from earlier findings
        should be explained.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">8. Conclusion</h1>
      <p>Identifying major research findings with some critical comments and comparison to the existing literatures. This
        covers all significant findings derived from the investigation. To ensure that the reader doesn't misunderstand,
        these should be well stated. Although it is frequently preferable to include conclusions in the discussion
        section, it may be beneficial to include a summary of findings in a different section for lengthy and complex
        papers.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">9. Acknowledgements</h1>
      <p>Any acknowledgments should be typed as text and placed before the references.</p>
    </div>
    <div class="space-y-2">
      <h1 class="font-semibold">10. References</h1>
      <p>All in-text citations, footnotes/endnotes and the reference page must follow the guidelines of the Publication
        Manual of the American Psychological Association (APA) 7th Edition.</p>
